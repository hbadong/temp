def get_pinyin(char: str) -> str:
    if not PINYIN_DATA:
        init_pinyin_data()
    return PINYIN_DATA.get(char, 'yi1')

def get_pinyin_with_tone(char: str) -> str:
    pinyin = get_pinyin(char)
    return pinyin

def parse_pinyin(pinyin: str) -> tuple:
    initial_map = {
        'b': 'b', 'p': 'p', 'm': 'm', 'f': 'f',
        'd': 'd', 't': 't', 'n': 'n', 'l': 'l',
        'g': 'g', 'k': 'k', 'h': 'h',
        'j': 'j', 'q': 'q', 'x': 'x',
        'zh': 'zh', 'ch': 'ch', 'sh': 'sh',
        'r': 'r', 'z': 'z', 'c': 'c', 's': 's',
        'y': 'y', 'w': 'w', 'l': 'l'
    }
    
    tone = 0
    for c in pinyin:
        if c.isdigit():
            tone = int(c)
            pinyin = pinyin.replace(c, '')
            break
    
    pinyin_lower = pinyin.lower()
    
    final = ''
    initial = ''
    for key in sorted(initial_map.keys(), key=len, reverse=True):
        if pinyin_lower.startswith(key):
            initial = key
            final = pinyin_lower[len(key):]
            break
    
    if not initial:
        final = pinyin_lower
        initial = ''
    
    return (initial, final, tone)

def analyze_yinyun(name: str) -> dict:
    pinyins = [get_pinyin(c) for c in name]
    initials = []
    finals = []
    tones = []
    
    for py in pinyins:
        initial, final, tone = parse_pinyin(py)
        initials.append(initial)
        finals.append(final)
        tones.append(tone)
    
    return {
        'pinyin': ' '.join(pinyins),
        'initials': initials,
        'finals': finals,
        'tones': tones
    }

